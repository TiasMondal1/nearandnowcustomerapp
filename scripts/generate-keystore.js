#!/usr/bin/env node
/**
 * Generate a release upload keystore for Android signing.
 *
 * Creates:
 *   android/app/upload-keystore.jks   (gitignored)
 *   android/keystore.properties       (gitignored)
 *
 * Safe to re-run only if the .jks does not already exist (we refuse to
 * overwrite — you never want to lose your upload key).
 *
 * Usage (from project root):
 *   npm run keystore:generate
 *   npm run keystore:generate -- --alias=upload --validity=10000
 *
 * Requires JDK 17+ on PATH (keytool ships with the JDK).
 */
const fs = require("fs");
const path = require("path");
const { spawnSync } = require("child_process");
const crypto = require("crypto");

const rootDir = path.resolve(__dirname, "..");
const appDir = path.join(rootDir, "android", "app");
const keystorePath = path.join(appDir, "upload-keystore.jks");
const propsPath = path.join(rootDir, "android", "keystore.properties");

if (fs.existsSync(keystorePath)) {
  console.error(`Refusing to overwrite existing keystore at:\n  ${keystorePath}`);
  console.error("If you really want to create a new one, delete that file first. BUT: once you");
  console.error("upload an APK signed with a key, you must keep using the same key forever for");
  console.error("Play Store updates. Losing it means you cannot ship updates.");
  process.exit(1);
}

const args = process.argv.slice(2).reduce((acc, raw) => {
  const m = raw.match(/^--([^=]+)=(.*)$/);
  if (m) acc[m[1]] = m[2];
  return acc;
}, {});

const alias = args.alias || "upload";
const validity = args.validity || "10000";
const dname = args.dname || "CN=Near and Now, OU=Customer App, O=Near and Now, L=Kolkata, S=West Bengal, C=IN";

function randomPassword() {
  return crypto.randomBytes(18).toString("base64").replace(/[^A-Za-z0-9]/g, "").slice(0, 20);
}

const storePassword = args.storePassword || randomPassword();
const keyPassword = args.keyPassword || storePassword;

const keytoolArgs = [
  "-genkeypair",
  "-v",
  "-keystore", keystorePath,
  "-alias", alias,
  "-keyalg", "RSA",
  "-keysize", "2048",
  "-validity", validity,
  "-storepass", storePassword,
  "-keypass", keyPassword,
  "-dname", dname,
];

console.log(`\nGenerating keystore:\n  ${keystorePath}\n  alias: ${alias}\n  validity: ${validity} days\n`);

const result = spawnSync("keytool", keytoolArgs, { stdio: "inherit" });

if (result.error) {
  console.error("\nkeytool failed to start. Is JDK installed and on PATH?");
  console.error(result.error.message || result.error);
  process.exit(1);
}
if ((result.status ?? 1) !== 0) {
  console.error("\nkeytool exited with error. Keystore was NOT created.");
  process.exit(result.status ?? 1);
}

const props = `# Auto-generated by scripts/generate-keystore.js — gitignored.
# DO NOT COMMIT. Back up this file AND upload-keystore.jks somewhere safe —
# losing the key means you can never ship an update to the Play Store.
storeFile=upload-keystore.jks
storePassword=${storePassword}
keyAlias=${alias}
keyPassword=${keyPassword}
`;
fs.writeFileSync(propsPath, props, { encoding: "utf8" });

console.log(`\nSuccess.`);
console.log(`  Keystore:   ${keystorePath}`);
console.log(`  Properties: ${propsPath}`);
console.log(`\nBack BOTH files up somewhere secure (password manager, encrypted drive).`);
console.log(`Do NOT commit them. They are already listed in android/.gitignore.`);
